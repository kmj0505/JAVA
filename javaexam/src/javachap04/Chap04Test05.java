package javachap04;

public class Chap04Test05 {

	public static void main(String[] args) {
		//for문을 이용해서
		//*
		//**
		//***
		//**** 만들기
		
		for(int i=0;i<4; i++)
		{
			System.out.print("*");
			for(int j=0; j<i; j++)
			{
				System.out.print("*");
				
			}
			System.out.print("\n");
		}
		
	}

}
